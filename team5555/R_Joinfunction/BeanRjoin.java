package com.javalec.R_Joinfunction;

public class BeanRjoin {
	
				//field
				 String name;
			     String id;
			     String pw;
			     String pw2;
			     String telno;
			     String email;
			     
			     
			     //constructor
				public BeanRjoin(String name, String id, String pw, String pw2, String telno, String email) {
					super();
					this.name = name;
					this.id = id;
					this.pw = pw;
					this.pw2 = pw2;
					this.telno = telno;
					this.email = email;
				}
			
				
				
				
				//method
			
				public String getName() {
					return name;
				}
			
			
				public void setName(String name) {
					this.name = name;
				}
			
			
				public String getId() {
					return id;
				}
			
			
				public void setId(String id) {
					this.id = id;
				}
			
			
				public String getPw() {
					return pw;
				}
			
			
				public void setPw(String pw) {
					this.pw = pw;
				}
			
			
				public String getPw2() {
					return pw2;
				}
			
			
				public void setPw2(String pw2) {
					this.pw2 = pw2;
				}
			
			
				public String getTelno() {
					return telno;
				}
			
			
				public void setTelno(String telno) {
					this.telno = telno;
				}
			
			
				public String getEmail() {
					return email;
				}
			
			
				public void setEmail(String email) {
					this.email = email;
				}
				
			
				
			


     
}//===============================
